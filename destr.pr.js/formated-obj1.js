
// Задача: У вас есть функция processUserData, 
// которая принимает объект с данными пользователя, включая имя, возраст и страну. 
// Вам необходимо реализовать функцию таким образом, чтобы она возвращала отформатированную строку, 
// содержащую информацию о пользователе, включая его имя и возраст. 
// Используйте деструктурирующее присваивание для извлечения имени и возраста из объекта.

// Функция для обработки данных пользователя
function processUserData(userData) {
  const { name, age } = userData;
  return `Пользователь ${name} возраста ${age} лет.`;
}

// Пример использования

// Объект с данными о пользователе
const user = { name: 'Анна', age: 30, country: 'Россия' };

// Обработка данных пользователя с помощью функции processUserData
const formattedInfo = processUserData(user);
console.log(formattedInfo);

// В этой задаче функция processUserData принимает объект userData, 
// содержащий имя и возраст пользователя.
// С помощью деструктурирующего присваивания мы извлекаем из этого объекта имя и возраст. 
// Затем мы используем эти значения для формирования строки с информацией о пользователе,
